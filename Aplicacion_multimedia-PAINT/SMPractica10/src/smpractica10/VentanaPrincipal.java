    /**
     * Aplica una transformación lineal a la intensidad de la imágen.
     * Se realiza una transformación por 3 puntos, siendo su objetivo modificar el brillo
     * y contraste en función de los valores a, b, c.
     * @param a primer tramo (0, a)
     * @param b segundo tramo (128, b)
     * @param c tercer tramo (255, c)
     */
    private void aplicarTransformacionLineal(int a, int b, int c) {
        VentanaInternaImagen vi = (VentanaInternaImagen) (escritorio.getSelectedFrame());
        if (vi != null) {
            if (imgFuente != null) {
                try {
                    byte funcionT[] = new byte[256];
                    for (int x = 0; x < 256; x++) {
                        if (x < 128)
                            funcionT[x] = (byte) ((b - a) * x / 128 + a);
                        else
                            funcionT[x] = (byte) ((c - b) * (x - 128) / 127 + b);
                    }
                    
                    LookupTable tabla = new ByteLookupTable(0, funcionT);
                    LookupOp lop = new LookupOp(tabla, null);
                    
                    BufferedImage imgdest = new BufferedImage(imgFuente.getWidth(), imgFuente.getHeight(), BufferedImage.TYPE_INT_RGB);
                    lop.filter(imgFuente, imgdest);
                    vi.getLienzo2D().setImage(imgdest);
                    vi.getLienzo2D().repaint();
                    
                } catch (IllegalArgumentException e) {
                    System.err.println(e.getLocalizedMessage());
                }
            }
        }
    }
    
    /**
     * Función encargada de devolver una imagen con la banda que le indiquemos.
     * @param img imagen que le pasamos
     * @param banda valor de la banda que buscamos
     * @return imagen con la banda indicada
     */
    private BufferedImage getImageBand(BufferedImage img, int banda) {
        //Creamos el modelo de color de la nueva imagen basado en un espcio de color GRAY
        ColorSpace cs = ColorSpace.getInstance(ColorSpace.CS_GRAY);
        ComponentColorModel cm = new ComponentColorModel(cs, false, false,Transparency.OPAQUE, DataBuffer.TYPE_BYTE);
        
        //Creamos el nuevo raster a partir del raster de la imagen original
        int vband[] = {banda};
        WritableRaster bRaster = (WritableRaster)img.getRaster().createWritableChild(0,0, img.getWidth(), img.getHeight(),0,0,vband);
        
        //Creamos una nueva imagen que contiene como raster el correspondiente a la banda
        return new BufferedImage(cm, bRaster, false, null);
    }
    
