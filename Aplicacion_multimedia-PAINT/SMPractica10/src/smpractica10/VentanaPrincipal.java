    private void AbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AbrirActionPerformed
        JFileChooser dlg = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Imágenes JPG, JPEG y PNG", "jpg", "jpeg", "png");
        dlg.setFileFilter(filtro);      
        
        int resp = dlg.showOpenDialog(this);

        if (resp == JFileChooser.APPROVE_OPTION) {
            try {
                File f = dlg.getSelectedFile();
                BufferedImage img = ImageIO.read(f);
                imgOriginal = new BufferedImage(img.getColorModel(), img.copyData(null), img.isAlphaPremultiplied(), null);
                VentanaInternaImagen vi = new VentanaInternaImagen();
                
                //2) Crear el objeto manejador
                //3) Enlazar el generador con el manejador
                vi.addInternalFrameListener(manejador);
                vi.getLienzo2D().addLienzoListener(manejador_lienzo);
               
                vi.getLienzo2D().setImage(img);
                this.escritorio.add(vi);
                vi.setTitle(f.getName());
                vi.setVisible(true);
                
                f = new File(getClass().getResource("/sonidos/fijar.wav").getFile());
                vi.getLienzo2D().setSonidoFijar(f);
                f = new File(getClass().getResource("/sonidos/eliminar.wav").getFile());
                vi.getLienzo2D().setSonidoEliminar(f);
            } catch (Exception ex) {
                System.err.println("Error al leer la imagen"+ ex.getLocalizedMessage());
            }
        }
    }//GEN-LAST:event_AbrirActionPerformed

