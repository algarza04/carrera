    private void espacioColorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_espacioColorActionPerformed
        VentanaInternaImagen vi = (VentanaInternaImagen) (escritorio.getSelectedFrame());
        
        if (vi != null) {
            BufferedImage img = vi.getLienzo2D().getImage();
            if(img!=null){
                ColorSpace cs = null;
                switch ((String) espacioColor.getSelectedItem()) {
                    case "RGB":
                        cs = ColorSpace.getInstance(ColorSpace.CS_sRGB);
                        break;
                    case "YCC":
                        cs = ColorSpace.getInstance(ColorSpace.CS_PYCC);
                        break;
                    case "GREY":
                        cs = GreyColorSpace.getInstance(GreyColorSpace.CS_GRAY);
                        break;
                    default:
                        return;
                }
                ColorModel cm = img.getColorModel();
                
                if (cm.getColorSpace().getType() == cs.getType()) {
                    System.out.println("La imagen ya está en el espacio de color seleccionado.");
                    return;
                }
                try{
                    ColorConvertOp cop = new ColorConvertOp(cs, null);
                    BufferedImage imgdest = cop.filter(img,null);
                    
                    vi = new VentanaInternaImagen();
                    vi.getLienzo2D().setImage(imgdest);
                    vi.setTitle((String) espacioColor.getSelectedItem());
                    escritorio.add(vi);
                    vi.setVisible(true);                    
                    
                } catch(IllegalArgumentException e){
                    System.err.println(e.getLocalizedMessage());
                }
            }
        }
    }//GEN-LAST:event_espacioColorActionPerformed

